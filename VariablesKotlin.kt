fun main(args : Array<String>) {

    var test: Number = 12.2
    println("$test")

    test = 12
    // Int smart cast from Number
    println("$test")

    test = 120L
    // Long smart cast from Number
    println("$test")

    val letter: Char
    letter = 'k'
    println("$letter")

    val flag = true
    println("$flag")
}